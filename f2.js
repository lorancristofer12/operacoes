let a = 10
let b = 12
let c = 15

let soma = (a - b) + c;
console.log(" o resultado da soma é:" + soma )

// 2. divisa0 

let d= 10
let e = 12
let f = 15

let divisao = (d - e) * (f + e) / 3;
console.log(" o resultado da divisão é:" + divisao );

// 3. soma geral (somaTotal)

let somaTotal = (soma + divisao)
console.log ("O resultado da minha soma total é:" + somaTotal );

// 4. subtração 

let g = 10;
let h = 12;
let i = 15;

let multiplicacao = ( (g + h * i) - (i - g)) * 5;
console.log ("O resultado da minha multiplicação é:" + multiplicacao );

// 5. Divisão da variável

let divisor = (multiplicacao / soma)
