// Verificando número

function verificarNumero(num) {
    if(num > 0) {
        console.log("O numero é positivo");
    }else if (num < 0) {
        console.log("O numero é negativo");
    }else {
        console.log("O numero é zero");
    }
}

verificarNumero(0);
verificarNumero(5);
verificarNumero(-9);